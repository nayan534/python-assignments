# Create list
list = [100,200,300,400,500]
print("Original List:", list)

# Access elements
print("First element:", list[2])

# Add elements
list.append(1000)
print("After Add:", list)

# Remove elements
list.remove(400)
print("After Remove:", list)

# Sort list
list.sort()
print("Sorted List:", list)

# Reverse list
list.reverse()
print("Reversed List:", list)
